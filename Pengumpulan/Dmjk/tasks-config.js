  // Inisialisasi tugas berdasarkan URL parameter
  function initializeTask() {
    const urlParams = new URLSearchParams(window.location.search);
    const taskId = urlParams.get('id') || '1';
    const config = TASKS_CONFIG[taskId];
    
    if (!config) {
      console.error('Task configuration not found');
      return;
    }
    
    // Set data-tugas attribute
    document.documentElement.setAttribute('data-tugas', taskId === 'UTS' || taskId === 'UAS' ? `Tugas${taskId}` : `Tugas${taskId}`);
    
    // Update page content
    document.title = `Upload ${config.title} - Desain dan Manajemen Jaringan Komputer`;
    document.getElementById('taskTitle').textContent = `Upload ${config.title} - Desain dan Manajemen Jaringan Komputer`;
    document.getElementById('fileLabel').textContent = config.fileLabel;
    document.getElementById('file').accept = config.mimeAccept;
    document.getElementById('fileInstructions').innerHTML = config.fileInstruction;
    document.getElementById('uploadButton').innerHTML = `Upload ${config.title}<div class="loading-spinner" id="spinner"></div>`;
  }
  
  // Call initialization on page load
  document.addEventListener('DOMContentLoaded', initializeTask);